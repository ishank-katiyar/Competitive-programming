int main() 
{
 ios_base::sync_with_stdio(false);
 cin.tie(nullptr);
 cout.tie(nullptr);
 int n;
 cin >> n;
 int Mx = 0, My = 0;
 for (int i = 0; i < n; i++) {
  char ch;
  cin >> ch;
  int x, y;
  cin >> x >> y;
  if (x > y) swap(x, y);
  if (ch == '+') {
   Mx = max(Mx, x);
   My = max(My, y);
  }
  else {
   bool ok = x >= Mx && y >= My;
   cout << (ok ? "YES" : "NO") << '\n';
  }
 }
 return 0;
}
